    // Test 1: Simple payload (no emulation prevention needed)
    vector<uint8_t> payload1 = {0x01, 0x02, 0x03, 0x04};
    auto nal1 = nal_pack(payload1, NAL_SLICE_IDR, 3);
    auto parsed1 = nal_parse(nal1);

    printf("-- Test 1: Simple payload\n");
